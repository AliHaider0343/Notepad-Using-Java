    public void undo_button() {
        if (careTaker.mementoList.size() > 0) {
            originator.getStateFromMemento(careTaker.undo());
            area.setText(originator.getState());
        }
        undone = true;
    }

    public void redo_button() {
        if (careTaker.mementoList.size() > 0) {
            originator.getStateFromMemento(careTaker.redo());
            area.setText(originator.getState());
        }

    }

