    private void jMenuItem15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem15ActionPerformed
        String title;
        do {
            title = JOptionPane.showInputDialog(null, "Enter The File Name To Save it As Given Name        .  . \n\n", "Notepad");
            if (title.equals("")) {
                JOptionPane.showMessageDialog(null, "Please Enter Valid Name Because Name Cannot be Empty           ", "NotePad", JOptionPane.ERROR_MESSAGE);
            }
        } while (title.equals(""));
        try {
            FileWriter fw = new FileWriter("c:\\" + title + ".txt");
            fw.write(area.getText());
            fw.close();

            JOptionPane.showMessageDialog(null, "                Successfully Saved With Name   " + title + "                    \n\n       FilePath(e:\\" + title + ".txt )       \n\n");

        } catch (IOException e) {
            System.out.println(e);
        }

// TODO add your handling code here:
    }//GEN-LAST:event_jMenuItem15ActionPerformed

